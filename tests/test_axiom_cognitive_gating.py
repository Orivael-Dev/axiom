# ── fixtures ──────────────────────────────────────────────────────────────────

_DOMAINS = {
    "engineering": (
        "Stochastic residual dithering reduces quantization error variance "
        "across model weights at low bits-per-weight during inference. "
    ),
    "personal": (
        "To roast the vegetables for dinner preheat the oven and toss with "
        "olive oil. Best served warm with gravy on the side. "
    ),
}


@pytest.fixture
def store_dir(tmp_path: Path) -> Path:
    """Build + install two domain packs; return the store base dir."""
    base = tmp_path
    store = base / "store"
    for domain, text in _DOMAINS.items():
        idx = base / f"{domain}_idx"
        ing = DomainIngester(domain=domain, index_dir=idx, session_id="build")
        doc = base / f"{domain}.txt"
        doc.write_text(text * 15)
        ing.ingest_file(doc)
        m = DomainPackManifest(
            name=f"{domain}-pack", title=domain, description=domain,
            version="1.0.0", author="t", license="Apache-2.0", domain=domain)
        pack = build_pack(manifest=m, index_dir=idx, output_dir=base / "packs")
        DomainPackStore(base_dir=store).install(pack)
    return store


def _pipeline(store_dir: Path, tmp_path: Path, **kw) -> CognitiveGatingPipeline:
    store = DomainPackStore(base_dir=store_dir)
    router = SemanticRouter(store)
    router.build_indexes()
    kc = KnowledgeCookieStore(tmp_path / "kc.json")
    defaults = dict(router=router, domain_store=store, knowledge_store=kc,
                    enable_intent=True, session_id="sess-test")
    defaults.update(kw)
    return CognitiveGatingPipeline(**defaults)


# ── construction / introspection ────────────────────────────────────────────────

class TestConstruction:
    def test_empty_pipeline_not_active(self):
        g = CognitiveGatingPipeline()
        assert g.active is False
        assert g.layers() == ["intent"]  # intent is on by default, no router/kc/hyde

    def test_router_makes_active(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        assert g.active is True
        assert "router" in g.layers()
        assert "knowledge" in g.layers()

    def test_session_id_autogenerated(self):
        g = CognitiveGatingPipeline()
        assert g.session_id.startswith("sess-")
        assert len(g.session_id) > 8

    def test_explicit_session_id_used(self):
        g = CognitiveGatingPipeline(session_id="my-session")
        assert g.session_id == "my-session"

    def test_hyde_disabled_without_rewriter(self):
        g = CognitiveGatingPipeline(enable_hyde=True, rewriter=None)
        assert g.enable_hyde is False  # can't HyDE without a rewriter

    def test_intent_can_be_disabled(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path, enable_intent=False)
        assert "intent" not in g.layers()


# ── routing ─────────────────────────────────────────────────────────────────────

class TestRouting:
    def test_engineering_query_routes_to_engineering(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        hits, tel = g.retrieve("how do I reduce quantization error in weights?", k=5)
        assert tel.routed_to == "engineering"

    def test_personal_query_routes_to_personal(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        hits, tel = g.retrieve("how long to roast vegetables for dinner?", k=5)
        assert tel.routed_to == "personal"

    def test_no_cross_domain_contamination(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        hits, tel = g.retrieve("quantization weight bits-per-weight inference", k=5)
        # every hit should come from the engineering pack index
        assert tel.routed_to == "engineering"
        assert all("personal" not in h.uri for h in hits)

    def test_routing_disabled_uses_fallback(self, tmp_path):
        class _FakeRetriever:
            def retrieve(self, q, *, k=5, domain=None, intent_filter=None):
                from axiom_research_retriever import RetrievedSource
                return [RetrievedSource(title="fb", uri="fallback/1", kind="x",
                                        score=1.0, snippet="fallback hit")]
        g = CognitiveGatingPipeline(fallback_retriever=_FakeRetriever())
        hits, tel = g.retrieve("anything", k=3)
        assert tel.routed_to == ""
        assert hits and hits[0].uri == "fallback/1"


