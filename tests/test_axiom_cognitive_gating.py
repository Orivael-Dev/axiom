# ── knowledge cookie wiring ──────────────────────────────────────────────────────

class TestKnowledgeRecording:
    def test_hits_recorded_into_cookie(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        _, tel = g.retrieve("quantization error reduction", k=5)
        assert tel.fragments_recorded > 0
        cookie = KnowledgeCookieStore(tmp_path / "kc.json").load()
        assert cookie is not None
        assert len(cookie.fragments) == tel.fragments_recorded

    def test_no_recording_without_cookie(self, store_dir, tmp_path):
        store = DomainPackStore(base_dir=store_dir)
        router = SemanticRouter(store); router.build_indexes()
        g = CognitiveGatingPipeline(router=router, domain_store=store,
                                    knowledge_store=None)
        _, tel = g.retrieve("quantization", k=5)
        assert tel.fragments_recorded == 0

    def test_cross_session_promotion(self, store_dir, tmp_path):
        kc_path = tmp_path / "kc.json"
        store = DomainPackStore(base_dir=store_dir)
        router = SemanticRouter(store); router.build_indexes()
        # Three distinct sessions hitting the same query → promotion at threshold 3
        for i in range(3):
            g = CognitiveGatingPipeline(
                router=router, domain_store=store,
                knowledge_store=KnowledgeCookieStore(kc_path),
                session_id=f"sess-{i}")
            g.retrieve("quantization error reduction in weights", k=5)
        final = CognitiveGatingPipeline(
            knowledge_store=KnowledgeCookieStore(kc_path), session_id="final")
        final.promote()
        ctx = final.extra_context()
        assert "hot_knowledge" in ctx

    def test_extra_context_empty_without_cookie(self):
        g = CognitiveGatingPipeline()
        assert g.extra_context() == {}

    def test_extra_context_empty_before_promotion(self, store_dir, tmp_path):
        g = _pipeline(store_dir, tmp_path)
        g.retrieve("quantization", k=5)  # one session, below threshold
        # single session → nothing promoted yet
        assert g.extra_context() == {}


