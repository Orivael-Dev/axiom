# ── HyDE expansion (mock backend) ───────────────────────────────────────────────

class _MockRewriter:
    """Stand-in QueryRewriter exposing rewrite_hyde()."""
    def __init__(self, answer: str = "", raise_err: bool = False):
        self._answer = answer
        self._raise = raise_err

    def rewrite_hyde(self, question, *, domain="general"):
        if self._raise:
            raise RuntimeError("backend down")
        return self._answer


class TestHyDE:
    def test_hyde_expands_query(self, store_dir, tmp_path):
        rw = _MockRewriter(answer="dense hypothetical answer with quantization "
                                  "residual dithering error variance vocabulary")
        g = _pipeline(store_dir, tmp_path, rewriter=rw, enable_hyde=True,
                      rewrite_domain="general")
        _, tel = g.retrieve("how to stop noise when shrinking weights", k=5)
        assert tel.hyde_used is True
        assert tel.hyde_source == "live-backend"
        assert "quantization" in tel.effective_query

    def test_hyde_falls_back_on_error(self, store_dir, tmp_path):
        rw = _MockRewriter(raise_err=True)
        g = _pipeline(store_dir, tmp_path, rewriter=rw, enable_hyde=True)
        _, tel = g.retrieve("how to shrink weights", k=5)
        assert tel.hyde_source == "fallback-original"
        assert tel.effective_query == "how to shrink weights"

    def test_hyde_falls_back_when_answer_equals_query(self, store_dir, tmp_path):
        rw = _MockRewriter(answer="how to shrink weights")  # identical → no expansion
        g = _pipeline(store_dir, tmp_path, rewriter=rw, enable_hyde=True)
        _, tel = g.retrieve("how to shrink weights", k=5)
        assert tel.hyde_used is False


# ── from_env wiring ─────────────────────────────────────────────────────────────

class TestFromEnv:
    def test_inactive_when_no_env(self, monkeypatch):
        for var in ("AXIOM_DOMAIN_STORE", "AXIOM_KNOWLEDGE_COOKIE", "AXIOM_HYDE"):
            monkeypatch.delenv(var, raising=False)
        g = CognitiveGatingPipeline.from_env()
        assert g.active is False

    def test_router_wired_from_env(self, store_dir, tmp_path, monkeypatch):
        monkeypatch.setenv("AXIOM_DOMAIN_STORE", str(store_dir))
        monkeypatch.delenv("AXIOM_KNOWLEDGE_COOKIE", raising=False)
        monkeypatch.delenv("AXIOM_HYDE", raising=False)
        g = CognitiveGatingPipeline.from_env()
        assert g.router is not None
        _, tel = g.retrieve("quantization weight inference", k=5)
        assert tel.routed_to == "engineering"

    def test_knowledge_wired_from_env(self, store_dir, tmp_path, monkeypatch):
        monkeypatch.setenv("AXIOM_DOMAIN_STORE", str(store_dir))
        monkeypatch.setenv("AXIOM_KNOWLEDGE_COOKIE", str(tmp_path / "envkc.json"))
        monkeypatch.delenv("AXIOM_HYDE", raising=False)
        g = CognitiveGatingPipeline.from_env()
        assert g.knowledge_store is not None
        _, tel = g.retrieve("quantization error", k=5)
        assert tel.fragments_recorded > 0

    def test_intent_disabled_via_env(self, store_dir, monkeypatch):
        monkeypatch.setenv("AXIOM_DOMAIN_STORE", str(store_dir))
        monkeypatch.setenv("AXIOM_GATING_INTENT", "0")
        g = CognitiveGatingPipeline.from_env()
        assert g.enable_intent is False


