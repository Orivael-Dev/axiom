def patch_guard_imports():
    echo("\n  Patching guard imports in package files...")

    OLD_PREFIX = "from axiom_constitutional.axiom_"
    NEW_PREFIX = "from axiom_constitutional.guards.axiom_"

    targets = [
        ROOT / "axiom_constitutional" / "__init__.py",
        ROOT / "axiom_constitutional" / "client.py",
        ROOT / "axiom_constitutional" / "certifier.py",
        ROOT / "axiom_certify.py",
        ROOT / "cli.py",
    ]
    for path in targets:
        if not path.exists():
            continue
        text = path.read_text(encoding="utf-8") if not DRY_RUN else ""
        guard_names = [n.replace(".py", "") for n in GUARD_FILES]
        needs_patch = any(
            f"from axiom_constitutional.{g}" in (text if not DRY_RUN else "x")
            for g in guard_names
        )
        if DRY_RUN or needs_patch:
            for name in guard_names:
                patch_file(
                    path,
                    f"from axiom_constitutional.{name} import",
                    f"from axiom_constitutional.guards.{name} import",
                    f"guard import: {name}",
                )

    # Fix self-import in axiom_review_queue.py (used in __main__ block)
    rq_path = ROOT / "axiom_constitutional" / "guards" / "axiom_review_queue.py"
    if rq_path.exists() or DRY_RUN:
        patch_file(
            rq_path,
            "from axiom_review_queue import ReviewRouter",
            "from axiom_constitutional.guards.axiom_review_queue import ReviewRouter",
            "review_queue self-import",
        )

