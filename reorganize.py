def patch_parser_subdir_search():
    """Patch axiom_files/parser.py to search core/ and research/ subdirs."""
    echo("\n  Patching axiom_files/parser.py for subdir search...")
    parser_path = ROOT / "axiom_files" / "parser.py"
    if not parser_path.exists():
        echo("  [skip] parser.py not found")
        return

    helper = '''
def _resolve_axiom_path(agent_name: str) -> str:
    """Resolve agent name to .axiom path, searching core/ and research/ subdirs."""
    base = agent_name.lower()
    # Strip subdir prefix if already included (e.g. "core/worker" -> "worker")
    if "/" in base:
        rel = base
        path = os.path.join(AXIOM_DIR, f"{rel}.axiom")
        if os.path.exists(path):
            return path
        # Try as-is without extension
        if os.path.exists(os.path.join(AXIOM_DIR, rel)):
            return os.path.join(AXIOM_DIR, rel)
    candidates = [
        os.path.join(AXIOM_DIR, f"{base}.axiom"),
        os.path.join(AXIOM_DIR, "core", f"{base}.axiom"),
        os.path.join(AXIOM_DIR, "research", f"{base}.axiom"),
    ]
    for p in candidates:
        if os.path.exists(p):
            return p
    return candidates[0]  # caller handles missing file

'''

    insert_marker = "AXIOM_DIR = os.environ.get"
    if DRY_RUN:
        echo(f"  patch   {parser_path.relative_to(ROOT)}  [add _resolve_axiom_path helper]")
        echo(f"  patch   {parser_path.relative_to(ROOT)}  [replace os.path.join(AXIOM_DIR, ...) calls]")
        return

    text = parser_path.read_text(encoding="utf-8")

    # Only inject if not already present
    if "_resolve_axiom_path" not in text:
        # Insert helper after the AXIOM_DIR = ... line
        idx = text.find(insert_marker)
        if idx != -1:
            end_of_line = text.index("\n", idx) + 1
            text = text[:end_of_line] + helper + text[end_of_line:]

    # Replace all os.path.join(AXIOM_DIR, f"{X.lower()}.axiom") patterns
    # with _resolve_axiom_path(X)
    pattern = re.compile(
        r'os\.path\.join\(AXIOM_DIR,\s*f"\{([^}]+?)\.lower\(\)\}\.axiom"\)'
    )
    text = pattern.sub(r'_resolve_axiom_path(\1)', text)

    # Also replace Path(AXIOM_DIR) / f"{X}.axiom" patterns (used in a few places)
    pattern2 = re.compile(
        r'Path\(AXIOM_DIR\)\s*/\s*f"\{([^}]+?)\.lower\(\)\}\.axiom"'
    )
    text = pattern2.sub(r'Path(_resolve_axiom_path(\1))', text)

    parser_path.write_text(text, encoding="utf-8")
    echo(f"  patched {parser_path.relative_to(ROOT)}  [subdir search]")

