# ── Helpers ──────────────────────────────────────────────────────────────────

def echo(msg):
    print(msg)

def git_mv(src: Path, dst: Path):
    """Move a file using git mv so history is preserved."""
    dst.parent.mkdir(parents=True, exist_ok=True)
    if DRY_RUN:
        echo(f"  git mv  {src.relative_to(ROOT)}  -> {dst.relative_to(ROOT)}")
        return
    result = subprocess.run(
        ["git", "mv", str(src), str(dst)],
        cwd=ROOT, capture_output=True, text=True
    )
    if result.returncode != 0:
        echo(f"  [warn] git mv failed ({result.stderr.strip()}), using shutil")
        shutil.move(str(src), str(dst))

def git_rm(path: Path):
    """Remove a file from git tracking and disk."""
    if DRY_RUN:
        echo(f"  git rm  {path.relative_to(ROOT)}")
        return
    subprocess.run(["git", "rm", "-f", str(path)], cwd=ROOT, capture_output=True)

def write_file(path: Path, content: str):
    if DRY_RUN:
        echo(f"  write   {path.relative_to(ROOT)}")
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")

def patch_file(path: Path, old: str, new: str, description: str):
    """Replace old with new in file, in-place."""
    if DRY_RUN:
        echo(f"  patch   {path.relative_to(ROOT)}  [{description}]")
        return
    text = path.read_text(encoding="utf-8")
    if old not in text:
        echo(f"  [skip]  patch not needed in {path.relative_to(ROOT)}  [{description}]")
        return
    path.write_text(text.replace(old, new), encoding="utf-8")
    echo(f"  patched {path.relative_to(ROOT)}  [{description}]")

def make_init(path: Path, exports: list[str] = None):
    """Create a __init__.py for a new subpackage."""
    content = '"""AXIOM subpackage — auto-generated by reorganize.py"""\n'
    if exports:
        for e in exports:
            content += f"{e}\n"
    write_file(path, content)

