# ── Main ─────────────────────────────────────────────────────────────────────

def main():
    global DRY_RUN
    parser = argparse.ArgumentParser(description="Reorganize axiom repo structure")
    parser.add_argument("--dry-run", action="store_true", help="Preview without making changes")
    args = parser.parse_args()
    DRY_RUN = args.dry_run

    if DRY_RUN:
        echo("=" * 60)
        echo("DRY RUN — no files will be modified")
        echo("=" * 60)
    else:
        echo("=" * 60)
        echo("EXECUTING reorganization...")
        echo("=" * 60)

    move_guards()
    patch_guard_imports()

    move_research_axioms()
    patch_research_pipeline()

    move_core_axioms()
    patch_parser_subdir_search()

    move_gamewatcher()
    move_docs()
    move_tests()
    move_examples()
    patch_pyproject()

    echo("\n" + "=" * 60)
    if DRY_RUN:
        echo("DRY RUN COMPLETE — run without --dry-run to execute")
    else:
        echo("DONE")
        echo("")
        echo("Next steps:")
        echo("  git add -A")
        echo('  git commit -m "refactor: clean repo structure v1.8.7 ..."')
        echo("  git push")
    echo("=" * 60)


if __name__ == "__main__":
    main()
